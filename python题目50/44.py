"""
【程序44】  
题目：一个偶数总能表示为两个素数之和。  
输入：一个偶数n（n>=4）  
输出：两个素数，使它们的和等于n。
素数定义：大于1的自然数，它只能被1和它本身整除。
实现算法:
1. 从2开始枚举素数，直到找到一个素数p，满足n-p为素数。
2. 输出p和n-p。
"""
n = int(input("请输入一个偶数n（n>=4）："))
# 检查n是否为偶数
if n % 2 != 0:
    print("输入的偶数必须为偶数")
    exit(1)
# 枚举素数p
for p in range(2, n // 2 + 1):
    # 检查p是否为素数
    if all(p % i != 0 for i in range(2, int(p**0.5) + 1)):
        # 检查n-p是否为素数
        if all((n-p) % i != 0 for i in range(2, int((n-p)**0.5) + 1)):
            print(f"{n} = {p} + {n-p}")
            break
            