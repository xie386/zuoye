"""
【程序37】  
题目：有n个人围成一圈，顺序排号。
从第一个人开始报数（从1到3报数），凡报到3的人退出圈子，问最后留下的是原来第几号的那位。  
1.程序分析：先将所有人的编号存储到一个列表中，再从第一个人开始报数，
凡报到3的人退出圈子，最后留下的是原来第几号的那位。  
"""
n = int(input("请输入人数："))
people = list(range(1, n + 1))

idx = 0
while len(people) > 1:
    idx = (idx + 2) % len(people)  # 每数3个，索引前进2位
    people.pop(idx)

print(f"最后留下的是原来第{people[0]}号")
