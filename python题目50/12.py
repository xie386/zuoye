"""
【程序12】  
题目：企业发放的奖金根据利润提成。
利润(I)低于或等于10万元时，奖金可提10%；
利润高于10万元，低于20万元时，低于10万元的部分按10%提成，高于10万元的部分，提成7.5%；
20万到40万之间时，高于20万元的部分，可提成5%；
40万到60万之间时，高于40万元的部分，可提成 3%；
60万到100万之间时，高于60万元的部分，可提成1.5%，
高于100万元时，超过100万元的部分按1%提成，从键盘输入当月利润I，求应发放奖金总数？  
"""
I = float(input("请输入利润：(单位：万元)"))

# 阶梯：每段 (区间长度, 提成比例)
brackets = [
    (10, 0.1),
    (10, 0.075),
    (20, 0.05),
    (20, 0.03),
    (40, 0.015),
]

bonus = 0
for length, rate in brackets:
    if I <= length:
        bonus += I * rate
        break
    bonus += length * rate
    I -= length
else:
    bonus += I * 0.01  # 超过100万的部分

print(f"奖金总数为：{bonus:.2f}万元")
